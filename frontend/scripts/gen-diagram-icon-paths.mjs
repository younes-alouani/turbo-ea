// Generates `src/features/diagrams/iconPaths.ts` — a committed map of
// Material Symbols icon name -> { d: SVG path data, vb: viewBox }, for the
// curated card-type icon set (see src/components/iconCatalog.ts).
//
// These paths are baked onto DrawIO diagram shapes as a corner image so the
// card-type icon shows on diagrams (accessibility: type by icon + colour, not
// colour alone). We ship real vector paths rather than the Material Symbols
// font because font glyphs cannot be reliably rasterised into images
// (see features/reports/reportExport.ts) and the DrawIO iframe has no access
// to the app's webfont.
//
// Two sources, in priority order, so every selectable picker icon is covered:
//   1. @material-symbols/svg-400 (outlined, weight 400) — the modern Material
//      Symbols set, snake_case names, viewBox "0 -960 960 960". Matches the
//      Material Symbols Outlined font the UI renders.
//   2. @mui/icons-material (already a dependency) — covers the handful of
//      *legacy Material Icons* names the picker still uses (email, business,
//      done, people, …) that the Symbols SVG package only ships as font
//      aliases. viewBox "0 0 24 24".
// A per-icon viewBox is stored because the two sources use different
// coordinate systems.
//
// Run with:  npm run gen:diagram-icons
// There is intentionally no pre-commit hook — regenerate and commit the output
// whenever icon names are added to iconCatalog.ts.

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

// Pull the curated icon-name list from the catalogue (single source of truth).
// Only read names inside `icons: [ ... ]` arrays so category labelKeys don't
// leak in. The module is plain TS with no runtime deps, so a tiny regex
// extraction avoids needing a TS loader here.
const catalogueSrc = readFileSync(
  resolve(root, "src/components/iconCatalog.ts"),
  "utf8",
);
const names = Array.from(
  new Set(
    [...catalogueSrc.matchAll(/icons:\s*\[([\s\S]*?)\]/g)]
      .flatMap((m) => m[1].match(/"[a-z0-9_]+"/g) || [])
      .map((s) => s.slice(1, -1)),
  ),
);

const symbolsDir = resolve(root, "node_modules/@material-symbols/svg-400/outlined");
const muiDir = resolve(root, "node_modules/@mui/icons-material");

/** Concatenate every <path d="..."> from an SVG / component-source string. */
function extractPaths(src) {
  // Matches both SVG markup (d="...") and MUI's compiled JSX (d: "...").
  const ds = [...src.matchAll(/\bd\s*[:=]\s*"([^"]+)"/g)].map((m) => m[1]);
  return ds.join(" ");
}

function pascalCase(name) {
  return name
    .split("_")
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join("");
}

/** Try the Material Symbols SVG (viewBox 0 -960 960 960). */
function fromSymbols(name) {
  const file = resolve(symbolsDir, `${name}.svg`);
  if (!existsSync(file)) return null;
  const d = extractPaths(readFileSync(file, "utf8"));
  return d ? { d, vb: "0 -960 960 960" } : null;
}

/** Fall back to the MUI outlined icon (viewBox 0 0 24 24). */
function fromMui(name) {
  const file = resolve(muiDir, `${pascalCase(name)}Outlined.js`);
  if (!existsSync(file)) return null;
  const d = extractPaths(readFileSync(file, "utf8"));
  return d ? { d, vb: "0 0 24 24" } : null;
}

const out = {};
const missing = [];
for (const name of names.sort()) {
  const entry = fromSymbols(name) || fromMui(name);
  if (entry) out[name] = entry;
  else missing.push(name);
}

const header = `// AUTO-GENERATED by scripts/gen-diagram-icon-paths.mjs — DO NOT EDIT BY HAND.
// Map of Material Symbols icon name -> SVG path data + viewBox.
// Regenerate with:  npm run gen:diagram-icons
// See the generator script for the rationale.

export interface IconPath {
  /** Concatenated SVG path data. */
  d: string;
  /** viewBox the path is drawn in (sources use different coordinate systems). */
  vb: string;
}

export const ICON_PATHS: Record<string, IconPath> = `;

const body = JSON.stringify(out, null, 2);
writeFileSync(
  resolve(root, "src/features/diagrams/iconPaths.ts"),
  `${header}${body};\n`,
  "utf8",
);

console.log(
  `Wrote ${Object.keys(out).length}/${names.length} icon paths to src/features/diagrams/iconPaths.ts`,
);
if (missing.length) {
  console.warn(
    `Uncovered (graceful fallback to no icon): ${missing.join(", ")}`,
  );
}
